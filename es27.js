let food=["apple", "mango", "banana"];
console.log(food);

//push
food.push("orange");
console.log(food);

//pop
food.pop();
console.log(food);

//unshift(it start to add element from the start)
food.unshift("pineapple");
console.log(food);

//shift (it start to remove element from the start)
food.shift();
console.log(food)
